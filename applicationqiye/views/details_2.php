<p>太上慈悲九幽拔罪忏</p>
<p>经名：太上慈悲九幽拔罪忏。十卷。底本出处：《正统道藏》洞玄部威仪类。</p>
<p>太上慈悲九幽拔罪忏卷之一</p>
<p>尔时元始天尊，时游十方世界，天上地下无所不经，至大福堂国，见虚皇道君，为十方国土、一切众生、善男女等，心生厌故，遂即分身应变，口吐五色琼光，遍照十方无不明彻。尔时男女及诸四众，昼夜惶惧，悲声叹泣，瞻仰尊颜，稽首顶礼，叩头博颡，肘行膝步。诸禽杂兽，并皆如是。一切众生，诸方仰视，寂绝形影，莫知所寻。尔时，虚皇道君，慈愍众生，分身下界，安坐碧霞神宝之座，念彼众生如常说法，演教化人。一切含灵，及诸禽兽，皆来诣座，稽首作礼，步步而前。重遇道君，欢喜踧踖。是时，座下有一真人，名曰普救，从众中出，稽首作礼，上白天尊，愿闻演说。十方男女，生可获福，死得受生，作何因缘，得遂本愿。
天尊告曰，汝可复坐，静念在心，一切因缘，吾为解说。天尊言，十方世界，皆有天堂。若有男女，常礼十方应号天尊，当生可获福，死得受生。於是普救真人，及诸四辈，长跪齐心，而听演说。尔时，天尊先说，东方青灵始老苍帝、九炁天君，华林境界，其有真人，名曰静境无极之国、惠静庄严。若人临终之日，愿生彼国，当礼东方应号天尊，长跪忏悔，免恒沙罪障，获无量善，因炼质澄神，登真悟道。众等至心，今当敬礼。</p>
