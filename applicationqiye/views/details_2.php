<p>礼三宝已，复次忏悔</p>
<p>志心归命，十方常住三宝。臣等忏悔累劫以来过误之罪。众等暗昧，不省前非，或造作恶缘，施为信意。心如火盛，炎炎而不拣媸妍；意似刀锋，凛凛而不论强弱。积时累日，经月逾年，未遇首陈，虑遭涂炭，冰池火柱，剑树镬汤，寒即痛心，热来焦体，断筋伤骨，腐腹糜肠。如此罪元，乞垂涤荡。
天尊言，若有善男子、善女人，未终之者，虽然造罪，亦复修福。在生修福，十分得八；将福补罪，多少始知。若当过去造诸功德，七分之中，惟获其一，计功补过，福乃有余，如此比量，始名得一。若将善功，补罪不足，即当涉历三涂，受诸苦恼。一日一夜，万死万生，备受诸苦。若有亡人，临终之日，诸方请师，及见在男女、香火知识同於一处，烧香忏悔，受戒诵经，先取亡人身上衣物，将对香火，忏悔受戒。从身亡之后，三七日内，别置清净之所，安立道场，燃灯续明，赞咏行道，鸣锺礼拜，虔恳投诚。所以尔者，急修功德。初亡之人，方开道路，得悟东西，乘此善缘，往生极乐。先亡获救，见在蒙恩。忏悔既终，志心敬礼。</p>
